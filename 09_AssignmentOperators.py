# =, +=, -=, *=

score = 0
print("Initially the value of score is ",score)

score+=1
print("After adding 1 to score , the value of score is ",score)

score -=1
print("After subtracting 1 from score, the value of score is ",score)

score*=3
print("After multiplying 3 to the score, the value of score is ",score)